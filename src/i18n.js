function translateValue(value, lang) {
  if (lang !== 'en') return value;
  const trimmed = value.trim();
  let translated = EN_TEXT[trimmed] || trimmed;
  EN_REPLACEMENTS.forEach(([pattern, replacement]) => {
    translated = translated.replace(pattern, replacement);
  });
  return translated;
}

export function applyInterfaceLanguage(lang) {
  if (typeof document === 'undefined') return;
  document.documentElement.lang = lang === 'en' ? 'en' : 'uk';
  if (lang !== 'en') return;

  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const parent = node.parentElement;
      if (!parent) return NodeFilter.FILTER_REJECT;
      if (['SCRIPT', 'STYLE', 'TEXTAREA', 'INPUT'].includes(parent.tagName)) return NodeFilter.FILTER_REJECT;
      if (!node.nodeValue.trim()) return NodeFilter.FILTER_REJECT;
      return NodeFilter.FILTER_ACCEPT;
    },
  });

  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach((node) => {
    const original = node.nodeValue;
    const leading = original.match(/^\s*/)?.[0] || '';
    const trailing = original.match(/\s*$/)?.[0] || '';
    node.nodeValue = `${leading}${translateValue(original, lang)}${trailing}`;
  });

  document.querySelectorAll('[title]').forEach((element) => {
    const title = element.getAttribute('title');
    const translated = translateValue(title, lang);
    if (translated !== title) element.setAttribute('title', translated);
  });

  document.querySelectorAll('input[placeholder]').forEach((element) => {
    const placeholder = element.getAttribute('placeholder');
    const translated = translateValue(placeholder, lang);
    if (translated !== placeholder) element.setAttribute('placeholder', translated);
  });
}
